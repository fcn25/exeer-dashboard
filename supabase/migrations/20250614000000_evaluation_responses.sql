-- Evaluation responses, cycle targeting, and assignment notifications

alter table public.evaluation_cycles
  add column if not exists template_id bigint references public.evaluation_templates (id) on delete set null;

alter table public.evaluation_cycles
  add column if not exists target_department text;

create index if not exists evaluation_cycles_template_id_idx
  on public.evaluation_cycles (template_id);

create table if not exists public.evaluation_responses (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies (id) on delete cascade,
  cycle_id bigint not null references public.evaluation_cycles (id) on delete cascade,
  employee_id bigint not null references public.employees (id) on delete cascade,
  answers jsonb not null default '{}'::jsonb,
  status text not null default 'pending',
  completed_at timestamptz,
  created_at timestamptz not null default now(),
  constraint evaluation_responses_status_check
    check (status in ('pending', 'completed')),
  constraint evaluation_responses_cycle_employee_unique
    unique (cycle_id, employee_id)
);

create index if not exists evaluation_responses_company_id_idx
  on public.evaluation_responses (company_id);

create index if not exists evaluation_responses_cycle_id_idx
  on public.evaluation_responses (cycle_id);

create index if not exists evaluation_responses_employee_id_idx
  on public.evaluation_responses (employee_id);

create index if not exists evaluation_responses_cycle_status_idx
  on public.evaluation_responses (cycle_id, status);

alter table public.evaluation_responses enable row level security;

drop policy if exists exeer_all_select on public.evaluation_responses;
drop policy if exists exeer_all_insert on public.evaluation_responses;
drop policy if exists exeer_all_update on public.evaluation_responses;
drop policy if exists exeer_all_delete on public.evaluation_responses;

create policy exeer_all_select on public.evaluation_responses for select using (true);
create policy exeer_all_insert on public.evaluation_responses for insert with check (true);
create policy exeer_all_update on public.evaluation_responses for update using (true);
create policy exeer_all_delete on public.evaluation_responses for delete using (true);

-- Extend notification types for evaluation assignments
alter table public.notifications
  drop constraint if exists notifications_type_check;

alter table public.notifications
  add constraint notifications_type_check check (
    type in (
      'request_approved',
      'new_employee',
      'subscription_alert',
      'evaluation_assigned'
    )
  );

-- Resolve auth user from employee email and insert notification (security definer)
create or replace function public.notify_evaluation_assignments(p_employee_ids bigint[])
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_employee_id bigint;
  v_user_id uuid;
  v_inserted integer := 0;
begin
  if p_employee_ids is null or array_length(p_employee_ids, 1) is null then
    return 0;
  end if;

  foreach v_employee_id in array p_employee_ids loop
    select u.id
    into v_user_id
    from public.employees e
    join auth.users u on lower(trim(u.email)) = lower(trim(e.email))
    where e.id = v_employee_id
      and e.email is not null
      and trim(e.email) <> ''
    limit 1;

    if v_user_id is not null then
      insert into public.notifications (user_id, title, message, type)
      values (
        v_user_id,
        'تقييم أداء جديد',
        'تم تعيين نموذج تقييم جديد لإدارتك. يرجى إكماله.',
        'evaluation_assigned'
      );
      v_inserted := v_inserted + 1;
    end if;
  end loop;

  return v_inserted;
end;
$$;

grant execute on function public.notify_evaluation_assignments(bigint[]) to authenticated;
