alter table public.companies
  add column if not exists sector_id bigint references public.sectors (id) on delete set null;

create index if not exists companies_sector_id_idx on public.companies (sector_id);

-- Map legacy industry text to sector_id where names align.
update public.companies c
set sector_id = s.id
from public.sectors s
where c.sector_id is null
  and c.industry is not null
  and trim(c.industry) = s.name_ar;

update public.companies c
set sector_id = s.id
from public.sectors s
where c.sector_id is null
  and c.industry = 'تقنية المعلومات'
  and s.name_ar = 'تقنية المعلومات';

alter table public.sectors enable row level security;

drop policy if exists sectors_select on public.sectors;
create policy sectors_select
  on public.sectors
  for select
  to authenticated
  using (true);

grant select on table public.sectors to authenticated;

notify pgrst, 'reload schema';
